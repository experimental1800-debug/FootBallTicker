const canonicalTeamKeyMap: Record<string, string> = {
  bosniaherzegovina: "bosniaandherzegovina",
  caboverde: "capeverdeislands",
  czechrepublic: "czechia",
  korearepublic: "southkorea",
  turkiye: "turkey",
  unitedstates: "usa",
};

export function normalizeTeamKey(team?: string) {
  const safeTeam = (team ?? "TBD").trim() || "TBD";
  const normalizedTeam = safeTeam
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/&/g, "and")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "");

  return canonicalTeamKeyMap[normalizedTeam] ?? normalizedTeam;
}

export function getTeamSlug(team: string) {
  return `${team
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/&/g, "and")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")}-national-football-team`;
}

export function getTeamPath(team: string) {
  return `/teams/${getTeamSlug(team)}`;
}

export function getTeamFlagUrl(team: string, width = 160) {
  const flagCode = teamFlagCodes[normalizeTeamKey(team)];
  return flagCode ? `https://flagcdn.com/w${width}/${flagCode}.png` : undefined;
}

export function getTeamLogoUrl(team: string) {
  return teamLogoUrls[normalizeTeamKey(team)];
}

export function getTeamImageUrl(team: string, flagWidth = 160) {
  return getTeamLogoUrl(team) ?? getTeamFlagUrl(team, flagWidth);
}

export function getTeamBySlug(slug: string, additionalTeams: string[] = []) {
  const candidates = Array.from(new Set([...worldCupTeams, ...additionalTeams]));
  return candidates.find((team) => getTeamSlug(team) === slug);
}
