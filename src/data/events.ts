export function buildFilterOptions(currentEvents: Event[]) {
  const validTeams = currentEvents
    .flatMap((event) => [event.homeTeam, event.awayTeam])
    .filter((team): team is string => Boolean(team?.trim()))
    .filter((team) => team !== "TBD");

  return {
    cities: [
      "Location",
      ...Array.from(
        new Set(currentEvents.map((event) => `${event.city}, ${event.state}`))
      ),
    ],
    dates: ["Date", ...Array.from(new Set(currentEvents.map((event) => event.date)))],
    teams: [
      "Team",
      ...Array.from(new Set(validTeams)),
    ],
  };
}
